#include <stdio.h>
#include <string.h>
#include <ctype.h>

int main()
{
    char name[50];
    int i, j, k;

    char pattern[26][5][6] =
    {
        {" *** ", "*   *", "*****", "*   *", "*   *"},
        {"**** ", "*   *", "**** ", "*   *", "**** "},
        {" ****", "*    ", "*    ", "*    ", " ****"},
        {"**** ", "*   *", "*   *", "*   *", "**** "},
        {"*****", "*    ", "**** ", "*    ", "*****"},
        {"*****", "*    ", "**** ", "*    ", "*    "},
        {" ****", "*    ", "* ***", "*   *", " ****"},
        {"*   *", "*   *", "*****", "*   *", "*   *"},
        {"*****", "  *  ", "  *  ", "  *  ", "*****"},
        {"*****", "   * ", "   * ", "*  * ", " **  "},
        {"*  * ", "* *  ", "**   ", "* *  ", "*  * "},
        {"*    ", "*    ", "*    ", "*    ", "*****"},
        {"*   *", "** **", "* * *", "*   *", "*   *"},
        {"*   *", "**  *", "* * *", "*  **", "*   *"},
        {" *** ", "*   *", "*   *", "*   *", " *** "},
        {"**** ", "*   *", "**** ", "*    ", "*    "},
        {" *** ", "*   *", "*   *", "*  * ", " ** *"},
        {"**** ", "*   *", "**** ", "* *  ", "*  * "},
        {" ****", "*    ", " *** ", "    *", "**** "},
        {"*****", "  *  ", "  *  ", "  *  ", "  *  "},
        {"*   *", "*   *", "*   *", "*   *", " *** "},
        {"*   *", "*   *", "*   *", " * * ", "  *  "},
        {"*   *", "*   *", "* * *", "** **", "*   *"},
        {"*   *", " * * ", "  *  ", " * * ", "*   *"},
        {"*   *", " * * ", "  *  ", "  *  ", "  *  "},
        {"*****", "   * ", "  *  ", " *   ", "*****"}
    };


    printf("Enter your name: ");
    scanf("%s", name);


    for(i = 0; i < 5; i++)
    {
        for(k = 0; k < strlen(name); k++)
        {
            int index = toupper(name[k]) - 'A';

            if(index >= 0 && index < 26)
            {
                for(j = 0; j < 5; j++)
                {
                    printf("%c", pattern[index][i][j]);
                }
            }

            printf("  ");
        }

        printf("\n");
    }


    return 0;
}
